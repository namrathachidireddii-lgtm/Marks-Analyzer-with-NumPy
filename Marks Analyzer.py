import numpy as np

marks = np.array([25, 60, 74, 82, 90])

total = np.sum(marks)

average = np.mean(marks)

highest = np.max(marks)

lowest = np.min(marks)

print("Total Marks:", total)
print("Average Marks:", average)
print("Highest Marks:", highest)
print("Lowest Marks:", lowest)


